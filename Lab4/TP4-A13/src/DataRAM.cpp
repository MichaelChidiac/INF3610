#include "DataRAM.h"

///////////////////////////////////////////////////////////////////////////////
//
//	Constructeur
//
///////////////////////////////////////////////////////////////////////////////
DataRAM::DataRAM( sc_module_name zName, const char *zDataFileName, unsigned long ulRAMSize, bool bVerbose )
:RAM(zName, zDataFileName, ulRAMSize, bVerbose)
{
}


///////////////////////////////////////////////////////////////////////////////
//
//	Destructeur
//
///////////////////////////////////////////////////////////////////////////////
DataRAM::~DataRAM()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//	Read
//
///////////////////////////////////////////////////////////////////////////////
unsigned char DataRAM::Read(unsigned int uiAddr)
{
	// On appele la classe parent
	return RAM::ReadChar(uiAddr);
}

	
///////////////////////////////////////////////////////////////////////////////
//
//	Write
//
///////////////////////////////////////////////////////////////////////////////
void DataRAM::Write(unsigned int uiAddr, unsigned char ucData)
{		
	if ( uiAddr >= m_ulRAMSize )
	{
		cout << "Écriture hors de la plage de la RAM" << endl;
		
		// Fin de la simulation
		sc_stop();
	}
	else
	{			
		// On copie le contenu de la mémoire
		memcpy( (void*)((unsigned int)(m_ptrRAM) + uiAddr), &ucData, 1);
	}
}


///////////////////////////////////////////////////////////////////////////////
//
//	end_of_simulation
//
///////////////////////////////////////////////////////////////////////////////
void DataRAM::end_of_simulation()
{
	// Variable
	FILE * pFile;
	unsigned long ulSize;
	
	// Ouverture du fichier de sortie
	pFile = fopen( this->name() , "wb" );
	
	// Validation de l'ouverture du fichier
	if (pFile == 0)
		cout << "Erreur lors de l'ouverture du fichier de sortie" << endl;
	else
	{
		// Écriture
		ulSize = fwrite(m_ptrRAM , 1 , m_ulRAMSize , pFile );
		
		if ( m_ulRAMSize != ulSize )
			cout << "La RAM n'a pas été toute sauvegardée dans le fichier de sortie" << endl;
		
		// Fermeture du fichier
		fclose(pFile);
	}
}
